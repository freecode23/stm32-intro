/**
 * Initialize UART peripheral at Pin2 and Pin3.
 */
void HAL_UART_MspInit(UART_HandleTypeDef *huart) {
	GPIO_InitTypeDef gpio_uart;

	// 1. Enable the clock for the USART2 peripheral as well as for GPIOA peripheral.
	__HAL_RCC_USART2_CLK_ENABLE();
	__HAL_RCC_GPIOA_CLK_ENABLE();

	// 2 . Do the pin muxing configurations for Pin2 (UART2_TX)
	gpio_uart.Pin = GPIO_PIN_2;

	// We need alternative functionality.
	gpio_uart.Mode = GPIO_MODE_AF_PP;
	gpio_uart.Pull = GPIO_PULLUP;
	gpio_uart.Speed = GPIO_SPEED_FREQ_LOW;
	gpio_uart.Alternate = GPIO_AF7_USART2;
	HAL_GPIO_Init(GPIOA, &gpio_uart);

	// 3. Set the same for Pin 3 (UART2_RX)
	gpio_uart.Pin = GPIO_PIN_3;
	HAL_GPIO_Init(GPIOA, &gpio_uart);

	// 4. Enable the IRQ and set up the priority.
	// allowing the NVIC (Nested Vectored Interrupt Controller) to accept interrupts generated by USART2.
	// For example, USART2 might generate an interrupt when it has finished sending/receiving data,
	// or when data is ready to be read from it.
	HAL_NVIC_EnableIRQ(USART2_IRQn);
	HAL_NVIC_SetPriority(USART2_IRQn, 15, 0);
}

