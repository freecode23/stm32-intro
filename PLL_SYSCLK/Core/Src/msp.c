#include "main_app.h"

void HAL_MspInit(void) {
	// 1. Set up the priority grouping of the arm cortex mx processor
	HAL_NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);

	// 2. Enable the required system exceptions of the arm cortex mx processor
	// The SHCSR enables the system handlers, and indicates:
	// the pending status of the BusFault, MemManage fault, and SVC exceptions
	// the active status of the system handlers.
	// Insert 111 into the bit 16, 17, 18.
	SCB->SHCSR |= 0x7 << 16; //usage fault, memory fault and bus fault system exceptions

	// 3. Configure the priority for the system exceptions. (Optional)
	// Highest priority for all.
	// Systick is already set up by hal init. Don't need to set priority here.
	HAL_NVIC_SetPriority(MemoryManagement_IRQn, 0, 0);
	HAL_NVIC_SetPriority(BusFault_IRQn, 0, 0);
	HAL_NVIC_SetPriority(UsageFault_IRQn, 0, 0);
}

void HAL_UART_MspInit(UART_HandleTypeDef *huart) {
	GPIO_InitTypeDef gpio_uart;

	// 1. Enable the clock for the USART2 peripheral as well as for GPIOA peripheral.
	__HAL_RCC_USART2_CLK_ENABLE();
	__HAL_RCC_GPIOA_CLK_ENABLE();

	// 2 . Do the pin muxing configurations for Pin2:
	gpio_uart.Pin = GPIO_PIN_2;

	// We need alternative functionality.
	gpio_uart.Mode = GPIO_MODE_AF_PP; // Alternate function push pull.
	gpio_uart.Pull = GPIO_PULLUP;
	gpio_uart.Speed = GPIO_SPEED_FREQ_LOW;
	gpio_uart.Alternate = GPIO_AF7_USART2; //UART2_TX
	HAL_GPIO_Init(GPIOA, &gpio_uart);

	// 3. Set the same for Pin 3:
	gpio_uart.Pin = GPIO_PIN_3; //UART2_RX
	HAL_GPIO_Init(GPIOA, &gpio_uart);

	// 4. Enable the IRQ and set up the priority.
	// allowing the NVIC (Nested Vectored Interrupt Controller) to accept interrupts generated by USART2.
	// For example, USART2 might generate an interrupt when it has finished sending/receiving data,
	// or when data is ready to be read from it.
	HAL_NVIC_EnableIRQ(USART2_IRQn);
	HAL_NVIC_SetPriority(USART2_IRQn, 15, 0);
}
